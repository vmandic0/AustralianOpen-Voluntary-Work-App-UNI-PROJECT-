    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        // TODO add your handling code here:
        String username = jTextFieldUsername.getText();
        String pass = new String(jPasswordField1.getPassword());
        User user = new User(username, pass);
        
        new Thread(() -> {
            try {
                Request req = new Request();
                req.setType(RequestType.LOGIN);
                req.setData(user);
                
                out.writeObject(req);
                out.flush();

                Response resp = (Response) in.readObject();
                JOptionPane.showMessageDialog(this, resp.getMessage());
                
                if (resp.isSuccess()) {
                    User loggedInUser = resp.getUser();
                    JFrame frame = new MainFormImaNalog(socket, in, out, loggedInUser);
                    Window parentWindow = SwingUtilities.getWindowAncestor(this);
                    if (parentWindow != null) {
                        parentWindow.dispose();
                    }
                    frame.setVisible(true);
                }

            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Gre≈°ka pri konekciji sa serverom");
            }
        }).start();
    }//GEN-LAST:event_jButtonLoginActionPerformed

